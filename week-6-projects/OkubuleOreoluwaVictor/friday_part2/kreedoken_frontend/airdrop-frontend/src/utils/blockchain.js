const AIRDROP_CONTRACT_ADDRESS = "0x9ea02f0bb38F4262154eDdB4BDf7d2561a68FBD7";
const TOKEN_CONTRACT_ADDRESS = "0xE6337571c096afE0569AfC40b6f44F5A5185C9e8";


import merkleData from '../../merkle-data.json';

export const connectWallet = async () => {
  if (window.ethereum) {
    try {
      const accounts = await window.ethereum.request({ 
        method: 'eth_requestAccounts' 
      });
      return accounts[0];
    } catch (error) {
      throw new Error('User rejected connection');
    }
  } else {
    throw new Error('Please install MetaMask');
  }
};

export const getProvider = () => {
  if (window.ethereum) {
    return new ethers.BrowserProvider(window.ethereum);
  }
  throw new Error('No Ethereum provider found');
};

export const getAirdropContract = async () => {
  const provider = getProvider();
  const signer = await provider.getSigner();
  return new ethers.Contract(AIRDROP_CONTRACT_ADDRESS, AIRDROP_ABI, signer);
};

export const getTokenContract = async () => {
  const provider = getProvider();
  const signer = await provider.getSigner();
  return new ethers.Contract(TOKEN_CONTRACT_ADDRESS, TOKEN_ABI, signer);
};

export const checkIfWhitelisted = (address) => {
  // Check if address exists in merkle data
  if (!address) return false;
  
  // Normalize address format to lowercase for comparison
  const normalizedAddress = address.toLowerCase();
  
  // Check if the address is in the whitelist
  return !!merkleData.whitelist[normalizedAddress];
};

export const getMerkleProof = (address) => {
  if (!address) return null;
  
  const normalizedAddress = address.toLowerCase();
  
  if (merkleData.whitelist[normalizedAddress]) {
    return merkleData.whitelist[normalizedAddress].proof;
  }
  
  return null;
};

export const claimTokens = async (address) => {
  try {
    const proof = getMerkleProof(address);
    
    if (!proof) {
      throw new Error('Address not in whitelist');
    }
    
    const airdropContract = await getAirdropContract();
    const tx = await airdropContract.claim(proof);
    return await tx.wait();
  } catch (error) {
    console.error('Claim error:', error);
    throw error;
  }
};

export const checkIfClaimed = async (address) => {
  try {
    const airdropContract = await getAirdropContract();
    return await airdropContract.hasClaimed(address);
  } catch (error) {
    console.error('Error checking claim status:', error);
    return false;
  }
};

export const getTokenBalance = async (address) => {
  try {
    const tokenContract = await getTokenContract();
    const balance = await tokenContract.balanceOf(address);
    const decimals = await tokenContract.decimals();
    const symbol = await tokenContract.symbol();
    
    // Format the balance with the correct decimal places
    const formattedBalance = ethers.formatUnits(balance, decimals);
    
    return {
      balance: formattedBalance,
      symbol
    };
  } catch (error) {
    console.error('Error getting token balance:', error);
    return { balance: '0', symbol: '' };
  }
};